package ie.deri.raul;

public class RaULException extends Exception {

	private static final long serialVersionUID = 4489458409863058371L;

	public RaULException() {
		super();
	}

	public RaULException(String message, Throwable cause) {
		super(message, cause);
	}

	public RaULException(String message) {
		super(message);
	}

	public RaULException(Throwable cause) {
		super(cause);
	}

}
